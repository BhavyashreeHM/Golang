package main

func constV() {
	//constant variable declarations in Go
	const pi float64 = 3.141592653589793
	println("Value of pi is:", pi)

	const greeting string = "Hello, Go!"
	println("Greeting message is:", greeting)

	const isGoFun bool = true
	println("Is Go fun?", isGoFun)

	const maxUsers = 1000
	println("Maximum number of users allowed:", maxUsers)

}
