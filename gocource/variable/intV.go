package main

func intV() {
	// different ways to declare integer variables in Go
	var number int
	number = 42
	println("The number is:", number)

	var number2 int = 100
	println("The second number is:", number2)

	var number3 = 256
	println("The third number is:", number3)

	number4 := 512
	println("The fourth number is:", number4)

}
